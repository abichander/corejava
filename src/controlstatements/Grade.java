package controlstatements;

public class Grade {

	public static void main(String[] args) {
		int grade=9;
		switch (grade) {
		case 10:{
			System.out.println("grade A+");
			break;
		}
		case 9:{
			System.out.println("grade A");
			break;
		}
		case 8:{
			System.out.println("grade B+");
			break;
			
		}case 7:{
			System.out.println("grade B");
			break;
		}
		
			
	
			

		default:{
			System.out.println("fail");
			break;
		}
			
		}
				

	}

}
