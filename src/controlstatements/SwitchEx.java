package controlstatements;

public class SwitchEx {

	public static void main(String[] args) {
		int a=11;
		switch (a) {
		case 1:{
			System.out.println("case 1");
			break;
		}
		case 2:{
			System.out.println("case 2");
			break;
			
		}
		case 11:{
			System.out.println("case 11");
			break;
		}
		default:{
			System.out.println("invalid");
		}
		}
		

	}

}
